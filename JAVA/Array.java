class Array{
	public static void main(String[] args){
		int[] arr = new int[3];
		System.out.println(arr);
		System.out.println(arr[0]);
		System.out.println(arr[1]);
		System.out.println(arr[2]);
		System.out.println(arr.length);
		
		String[] cha = new String[3];
		System.out.println(cha);
		System.out.println(cha[0]);
		System.out.println(cha[1]);
		System.out.println(cha[2]);
		System.out.println(cha.length);
		
		double[] dou = new double[3];
		System.out.println(dou);
		System.out.println(dou[0]);
		System.out.println(dou[1]);
		System.out.println(dou[2]);
		System.out.println(dou.length);
		
		byte[] byt = new byte[3];
		System.out.println(byt);
		System.out.println(byt[0]);
		System.out.println(byt[1]);
		System.out.println(byt[2]);
		System.out.println(byt.length);
		
		char[] ch = new char[3];
		System.out.println(ch);
		System.out.println(ch[0]);
		System.out.println(ch[1]);
		System.out.println(ch[2]);
		System.out.println(ch.length);
	}
}
		
		